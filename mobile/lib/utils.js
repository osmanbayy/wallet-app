export function formatDate(dateString) {
  // format date nicely
  // example: from this ðŸ‘‰ 2025-05-20 to this ðŸ‘‰ May 20, 2025
  const date = new Date(dateString);
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}

